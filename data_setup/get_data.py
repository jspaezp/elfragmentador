def get_irt_calculator(unmod_df):
    """
    Gets a function that calculates iRTs

    Uses the following peptides as refernce for the retention times

    "ISLGEHEGGGK": 0
    "GFVIDDGLITK": 100

    Examples
    ========

    >>> irt_calculator = get_irt_calculator(unmod_df2)
    >>> irt_calculator(np.array([45, 15]))
    array([104.17365452, -10.17304467])
    """

    rt_values = np.array(
        [
            unmod_df[unmod_df["Modified sequence"] == "_ISLGEHEGGGK_"][
                "Calibrated retention time"
            ],
            unmod_df[unmod_df["Modified sequence"] == "_GFVIDDGLITK_"][
                "Calibrated retention time"
            ],
        ]
    )

    s_perirt = (rt_values[1] - rt_values[0]) / 100  # Num of minutes per irt score

    def irt_calculator(rt):
        irt = (rt - rt_values[0]) / s_perirt
        return irt

    return irt_calculator


def process_dir(in_dir: Path, out_dir: Path = Path("."), prefix: str = ""):
    """
    process_dir

    From a directory with search results, gets all accepted protein groups,
    removes contaminants and decoys, calculates the iRT for each run (folder),
    encodes the peptide and splits the table with and without modifications.

    It also keeps just the 'evidence id' with the highest intensity

    Parameters
    ----------
    in_dir : Path
        Input directory to process
    out_dir : Path, optional
        Ouput directory, by default Path(".")
    prefix : str, optional
        Prefix to be added to the output files, by default ""

    Returns
    -------
        It returns two pandas data frames, with the modified and unmodified peptides
    """
    df = pd.read_csv(in_dir / "./proteinGroups.txt", sep="\t")
    df = df[df["Potential contaminant"] != "+"]
    evidence_df = pd.read_csv(in_dir / "evidence.txt", sep="\t")

    evd_ids = [x.split(";") for x in df["Evidence IDs"]]
    flat_list = [item for sublist in evd_ids for item in sublist]
    flat_list = [int(e) for e in flat_list]

    evidence_sec = evidence_df[[e in flat_list for e in evidence_df["id"]]]
    df = evidence_sec[
        [
            "MS/MS Count",
            "Intensity",
            "Calibrated retention time",
            "Modified sequence",
            "Charge",
            "Leading proteins",
            "Raw file",
        ]
    ]

    idx = (
        df.groupby(["Modified sequence"])["Intensity"].transform(max) == df["Intensity"]
    )
    df2 = df[idx]

    mod_idxs = ["(" in x for x in df2["Modified sequence"]]
    mod_df2 = df2[mod_idxs].copy()
    unmod_df2 = df2[[not x for x in mod_idxs]].copy()
    irt_calculator = get_irt_calculator(unmod_df2)

    def add_cols(df: pd.DataFrame, irt_calculator: Callable, encode: bool = True):
        """
        Adds encoding and irt given an irt calculator

        Parameters
        ----------
            df: peptide data frame, requires the 'Modified sequence'
                and 'Calibrated retention time' columns
            irt_calculator:

        Returns
        -------
            The same data frame with the added columns
        """
        if encode:
            df["Encoding"] = [str(encode_mod_seq(m)) for m in df["Modified sequence"]]
        df["iRT"] = irt_calculator(np.array(df["Calibrated retention time"]))
        df["Sequence Length"] = [len(x) - 2 for x in df["Modified sequence"]]

        return df

    unmod_df2 = add_cols(unmod_df2, irt_calculator)
    mod_df2 = add_cols(mod_df2, irt_calculator, encode=False)

    unmod_df2.to_csv(out_dir / (prefix + "unmodified_irt.csv"), index=False)
    mod_df2.to_csv(out_dir / (prefix + "modified_irt.csv"), index=False)

    return unmod_df2, mod_df2


